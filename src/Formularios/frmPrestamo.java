    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        String msj;
        ClasePrestamos prestamo = new ClasePrestamos();
        prestamo.setNumeroPrestamos(Integer.parseInt(txtNumPrestamo.getText()));
        prestamo.setNombreCliente(txtNombre.getText());
        
        if(btnPersonal.isSelected()){
            msj = objPersonal.toString();
            prestamo.setTipoPrestamo(1);
        }else{
            msj = objHipotecario.toString();
            prestamo.setTipoPrestamo(2);
        }
        
        try{
            pps = cn.prepareStatement("INSERT INTO prestamo(idPrestamo, nombreCliente, tipoPrestamo)"
                    + " VALUES(?, ?, ?)");
            
            pps.setInt(1, prestamo.getNumeroPrestamos());
            pps.setString(2, prestamo.getNombreCliente());
            pps.setInt(3, prestamo.getTipoPrestamo());
            
            pps.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Prestamo Agregado con Exito.");
        }catch(SQLException ex){
            java.util.logging.Logger.getLogger(ClasePrestamos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(btnPersonal.isSelected()){
            AggPersonal();
        }else{
            AggHipotecario();
        }
        JOptionPane.showMessageDialog(rootPane, msj);
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        String where = "";
        
        if(!"".equals(codigo)){
            where = " WHERE idPrestamo = '"+ codigo +"'";
        }
        
        try{
            DefaultTableModel modelo = new DefaultTableModel();
            tblPrestamos.setModel(modelo);
            
            String SQL = "SELECT * FROM prestamo " + where;
            pps = cn.prepareStatement(SQL);
            rs = pps.executeQuery();
            
            java.sql.ResultSetMetaData rsMD = rs.getMetaData();
            int cantidadColumnas = rsMD.getColumnCount();
            
            modelo.addColumn("Numero");
            modelo.addColumn("Cliente");
            modelo.addColumn("Tipo");
            
            while(rs.next()){
                Object[] filas = new Object[cantidadColumnas];
                
                for(int i=0; i<cantidadColumnas; i++){
                    filas[i] = rs.getObject(i+1);
                }
                
                modelo.addRow(filas);
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error " + ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

