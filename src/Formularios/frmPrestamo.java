    private void AggPersonal(){
        try{
            pps = cn.prepareStatement("INSERT INTO detallepersonal(aval1, aval2, sueldo, Prestamo)"
                    + " VALUES(?, ?, ?, ?)");
            
            pps.setString(1, objPersonal.getAval1());
            pps.setString(2, objPersonal.getAval2());
            pps.setDouble(3, objPersonal.getSueldo());
            pps.setInt(4, objPersonal.getNumeroPrestamos());
            
            pps.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Prestamo Agregado con Exito.");
            Limpiar();
        }catch(SQLException ex){
            java.util.logging.Logger.getLogger(ClasePrestamos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
